# Basic Operations
a = 10
b = 3
print("Addition:", a + b)
print("Substraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Mod:", a % b)
print("Double Division:", a // b)
print("Empowerment:", a ** b)

# Shortcuts
a += 2
print(a)
a -= 2
print(a)
a *= 2
print(a)
a /= 2
print(a)

# Boolean operations
print(a>b)
print(a<b)
print(a>=b)
print(a<=b)
print(a==b)
print(a!=b)